// ######## ~ L'addition ~ ######## //
// Déclaration de plusieurs variables, à la suite....
var nb1, nb2, resultat;

nb1 = 10;
nb2 = 5;

// -- L'addition de nb1 + nb2 avec l'opérateur " + "
resultat = nb1 + nb2;
console.log("L'Addition de nb1 et nb2 est égale à : " + resultat);

// ######## ~ La Soustraction ~ ######## //

// -- La soustraction de nb1 + nb2 avec l'opérateur " - "
resultat = nb1 - nb2;
console.log("La soustraction de nb1 et nb2 est égale à : " + resultat);

// ######## ~ La Multiplication ~ ######## //

// -- La multiplication de nb1 + nb2 avec l'opérateur " * "
resultat = nb1 * nb2;
console.log("La multiplication de nb1 et nb2 est égale à : " + resultat);

// ######## ~ La Division ~ ######## //

// -- La division de nb1 + nb2 avec l'opérateur " / "
resultat = nb1 / nb2;
console.log("La division de nb1 et nb2 est égale à : " + resultat);

// ######## ~ Le Modulo ~ ######## //

// -- le modulo de nb1 + nb2 avec l'opérateur " / "
nb1 = 11;
resultat = nb1 % nb2;
console.log("Le Modulo de nb1 et nb2 est égale à : " + resultat);

